public class ResortInfo {

    String name;
    int degrees;
    int wind;
    int distance;
    String imageUrl;

    public ResortInfo(String name, int degrees, int wind, int distance, String imageUrl) {
        this.name = name;
        this.degrees = degrees;
        this.wind = wind;
        this.distance = distance;
        this.imageUrl = imageUrl;
    }
}
